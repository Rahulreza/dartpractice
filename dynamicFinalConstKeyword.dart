void main(List<String> args) {
  //dynamic keyword
  dynamic name = "Rahul";
  print(name);
  //final or constant is used for not to change variables value
  var n = 20;
  n = 43;
  print("value change ${n}");

  final variablefinal = 59;
  print(variablefinal);

  const constvariable = 30;
  print(constvariable);
}
